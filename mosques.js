// Search and filter functionality
function displayMosques(mosquesToDisplay) {
    const grid = document.getElementById('mosquesGrid');
    if (!grid) return;
    
    grid.innerHTML = mosquesToDisplay.map(mosque => `
        <div class="mosque-card">
            <h3>${mosque.name}</h3>
            <p class="address">${mosque.address}, ${mosque.postcode}</p>
            <div class="prayer-times-grid">
                <div class="prayer-time">
                    <span class="label">Fajr</span>
                    <span class="time">${mosque.fajr}</span>
                </div>
                <div class="prayer-time">
                    <span class="label">Dhuhr</span>
                    <span class="time">${mosque.dhuhr}</span>
                </div>
                <div class="prayer-time">
                    <span class="label">Asr</span>
                    <span class="time">${mosque.asr}</span>
                </div>
                <div class="prayer-time">
                    <span class="label">Maghrib</span>
                    <span class="time">${mosque.maghrib}</span>
                </div>
                <div class="prayer-time">
                    <span class="label">Isha</span>
                    <span class="time">${mosque.isha}</span>
                </div>
            </div>
        </div>
    `).join('');
}

function setupSearch() {
    const searchInput = document.getElementById('searchInput');
    if (!searchInput) return;

    searchInput.addEventListener('input', (e) => {
        const searchTerm = e.target.value.toLowerCase();
        const filtered = mosques.filter(mosque => 
            mosque.name.toLowerCase().includes(searchTerm) || 
            mosque.address.toLowerCase().includes(searchTerm) ||
            mosque.postcode.toLowerCase().includes(searchTerm)
        );
        displayMosques(filtered);
    });
}

